function fun = somefunnyname(a,b,h)
    x=[a:b:h];
    fun = 2.*sin(8.*x)-log(x.^2+1);
end

function funfun = evenfunniername(f,a,b,h)
    x=[a:b:h];
    funfun=f(x)
end

function A = area(a,b)
    switch nargin
        case 1
            A=a.^2
        case 2
            A=a.*b
    end
end